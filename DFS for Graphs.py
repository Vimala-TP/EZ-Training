#DFS for Graphs
Al={1:[(1,2,0),(1,3,0)],
    2:[(2,1,0),(2,7,0)],
    3:[(3,1,0),(3,5,0),(3,6,0)],
    4:[(4,7,0),(4,8,0)],
    5:[(5,3,0),(5,7,0)],
    6:[(6,3,0),(6,8,0)],
    7:[(7,2,0),(7,4,0),(7,5,0)],
    8:[(8,4,0),(8,6,0)]
    }
V={1:'F',
   2:'F',
   3:'F',
   4:'F',
   5:'F',
   6:'F',
   7:'F',
   8:'F'
   }
S=[]
def DFS(Al,V,S,E):
    if V[E]=='F':
        S.append(E)
        V[E]='T'
    else:
        return
    for i in Al[E]:
        DFS(Al,V,S,i[1])
    print(S.pop(),end=' ')
DFS(Al,V,S,1)